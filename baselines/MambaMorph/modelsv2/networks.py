import numpy as np
import torch
import torch.nn as nn


def default_unet_features():
    nb_features = [
        [16, 32, 32, 32],  # encoder
        [32, 32, 32, 32, 32, 16, 16]  # decoder
    ]
    return nb_features


class Unet(nn.Module):
    """
    A unet architecture. Layer features can be specified directly as a list of encoder and decoder
    features or as a single integer along with a number of unet levels. The default network features
    per layer (when no options are specified) are:

        encoder: [16, 32, 32, 32]
        decoder: [32, 32, 32, 32, 32, 16, 16]
    """

    def __init__(self,
                 inshape=None,
                 infeats=None,
                 nb_features=None,
                 nb_levels=None,
                 max_pool=2,
                 feat_mult=1,
                 nb_conv_per_level=1,
                 half_res=False,
                 norm=False,
                 conv_1_1=False):
        """
        Parameters:
            inshape: Input shape. e.g. (192, 192, 192)
            infeats: Number of input features.
            nb_features: Unet convolutional features. Can be specified via a list of lists with
                the form [[encoder feats], [decoder feats]], or as a single integer. 
                If None (default), the unet features are defined by the default config described in 
                the class documentation.
            nb_levels: Number of levels in unet. Only used when nb_features is an integer. 
                Default is None.
            feat_mult: Per-level feature multiplier. Only used when nb_features is an integer. 
                Default is 1.
            nb_conv_per_level: Number of convolutions per unet level. Default is 1.
            half_res: Skip the last decoder upsampling. Default is False.
            conv_1_1: If prev_nf != nf, use 1x1 conv. This function is used to compare with node.
        """

        super().__init__()

        # ensure correct dimensionality
        ndims = len(inshape)
        assert ndims in [1, 2, 3], 'ndims should be one of 1, 2, or 3. found: %d' % ndims

        # cache some parameters
        self.half_res = half_res

        # default encoder and decoder layer features if nothing provided
        if nb_features is None:
            nb_features = default_unet_features()

        # build feature list automatically
        if isinstance(nb_features, int):
            if nb_levels is None:
                raise ValueError('must provide unet nb_levels if nb_features is an integer')
            feats = np.round(nb_features * feat_mult ** np.arange(nb_levels)).astype(int)
            nb_features = [
                np.repeat(feats[:-1], nb_conv_per_level),
                np.repeat(np.flip(feats), nb_conv_per_level)
            ]
        elif nb_levels is not None:
            raise ValueError('cannot use nb_levels if nb_features is not an integer')

        # extract any surplus (full resolution) decoder convolutions
        enc_nf, dec_nf = nb_features
        nb_dec_convs = len(enc_nf)
        final_convs = dec_nf[nb_dec_convs:]
        dec_nf = dec_nf[:nb_dec_convs]
        self.nb_levels = int(nb_dec_convs / nb_conv_per_level) + 1

        if isinstance(max_pool, int):
            max_pool = [max_pool] * self.nb_levels

        # cache downsampling / upsampling operations
        MaxPooling = getattr(nn, 'MaxPool%dd' % ndims)
        self.pooling = [MaxPooling(s) for s in max_pool]
        self.upsampling = [nn.Upsample(scale_factor=s, mode='nearest') for s in max_pool]

        # configure encoder (down-sampling path)
        prev_nf = infeats
        encoder_nfs = [prev_nf]
        self.encoder = nn.ModuleList()
        for level in range(self.nb_levels - 1):
            convs = nn.ModuleList()
            for conv in range(nb_conv_per_level):
                nf = enc_nf[level * nb_conv_per_level + conv]
                if prev_nf != nf and conv_1_1:
                    convs.append(ConvBlock(ndims, prev_nf, nf, norm=norm, kernel=1, padding=0))
                    convs.append(ConvBlock(ndims, nf, nf, norm=norm))
                else:
                    convs.append(ConvBlock(ndims, prev_nf, nf, norm=norm))
                prev_nf = nf
            self.encoder.append(convs)
            encoder_nfs.append(prev_nf)

        # configure decoder (up-sampling path)
        encoder_nfs = np.flip(encoder_nfs)
        self.decoder = nn.ModuleList()
        for level in range(self.nb_levels - 1):
            convs = nn.ModuleList()
            for conv in range(nb_conv_per_level):
                nf = dec_nf[level * nb_conv_per_level + conv]
                if prev_nf != nf and conv_1_1:
                    convs.append(ConvBlock(ndims, prev_nf, nf, norm=norm, kernel=1, padding=0))
                    convs.append(ConvBlock(ndims, nf, nf, norm=norm))
                else:
                    convs.append(ConvBlock(ndims, prev_nf, nf, norm=norm))
                prev_nf = nf
            self.decoder.append(convs)
            if not half_res or level < (self.nb_levels - 2):
                prev_nf += encoder_nfs[level]

        # now we take care of any remaining convolutions
        self.remaining = nn.ModuleList()
        for num, nf in enumerate(final_convs):
            if prev_nf != nf and conv_1_1:
                self.remaining.append(ConvBlock(ndims, prev_nf, nf, norm=norm, kernel=1, padding=0))
                self.remaining.append(ConvBlock(ndims, nf, nf, norm=norm))
            else:
                self.remaining.append(ConvBlock(ndims, prev_nf, nf, norm=norm))
            prev_nf = nf

        # cache final number of features
        self.final_nf = prev_nf

    def forward(self, x):
        # encoder forward pass
        x_history = [x]
        for level, convs in enumerate(self.encoder):
            for conv in convs:
                x = conv(x)
            x_history.append(x)
            x = self.pooling[level](x)

        # decoder forward pass with upsampling and concatenation
        for level, convs in enumerate(self.decoder):
            for conv in convs:
                x = conv(x)
            if not self.half_res or level < (self.nb_levels - 2):
                x = self.upsampling[level](x)
                x = torch.cat([x, x_history.pop()], dim=1)

        # remaining convs at full resolution
        for conv in self.remaining:
            x = conv(x)

        return x



class ConvBlock(nn.Module):
    """
    Specific convolutional block followed by leakyrelu for unet.
    """

    def __init__(self, ndims, in_channels, out_channels, kernel=3,
                 stride=1, padding=1, norm=False, act=True):
        super().__init__()
        self.norm = norm
        Conv = getattr(nn, 'Conv%dd' % ndims)
        self.main = Conv(in_channels, out_channels, kernel, stride, padding)
        self.act = act
        if self.norm:
            BatchNorm = getattr(nn, 'BatchNorm%dd' % ndims)
            self.batch_norm = BatchNorm(out_channels)
        if self.act:
            self.activation = nn.LeakyReLU(0.2)
        torch.nn.init.kaiming_normal_(self.main.weight)
        torch.nn.init.constant_(self.main.bias, 0)

    def forward(self, x):
        out = self.main(x)
        if self.norm:
            out = self.batch_norm(out)
        if self.act:
            out = self.activation(out)
        return out


class HybridFeatureExtractor(nn.Module):
    def __init__(self, dim, img_size, nb_features = [[16] * 2, [16] * 4]):
        super().__init__()
        
        self.feature_extractor = Unet(inshape=img_size,
                                      infeats=1,
                                      nb_features=nb_features,
                                      nb_levels=None,
                                      feat_mult=1,
                                      nb_conv_per_level=1,
                                      half_res=False)
        self.proj_x = nn.Conv3d(in_channels=nb_features[-1][-1], out_channels=dim, kernel_size=1)
        self.proj_y = nn.Conv3d(in_channels=nb_features[-1][-1], out_channels=dim, kernel_size=1)
        self.conv_xyz = nn.Conv3d(in_channels=3*dim, out_channels=dim, kernel_size=1)

    def forward(self, x, y):

        B, C = x.shape[:2]
        
        x_feat = self.feature_extractor(x)
        y_feat = self.feature_extractor(y)
        x_proj = self.proj_x(x_feat)
        y_proj = self.proj_y(y_feat)
        n_tokens = x_proj.shape[2:].numel()
        img_dims = x_proj.shape[2:]
        # using matrix multiplication to get the local interaction
        x_proj = x_proj.reshape(B, n_tokens, -1)
        y_proj = y_proj.reshape(B, n_tokens, -1)
        xy = x_proj @ y_proj.transpose(-1, -2) # TODO: memory boom！！！
        xyz = torch.cat([x_proj, y_proj, xy], dim=-1).permute(0, 2, 1).reshape(B, -1, *img_dims)
        hybrid_feat = self.conv_xyz(xyz)
        return hybrid_feat
    
